# For Loops. Range (start, stop, step)

for x in range(0,10,1):     # imprime de 0 a 9
    print(x)

for x in range(1,11,1):     # imprime de 1 a 10
    print(x)

for x in range(0,11,2):    # imprime los pares de 0 a 10
    print(x)