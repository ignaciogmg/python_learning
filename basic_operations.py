# Basic Operators & Input

# Input and printing
nombre = input("Cómo te llamas? ")
age = input("Cuál es tu edad? ")
print("Mi edad es", age, "y me llamo", nombre)  # Una forma de hacerlo
print(f"Mi edad es {age} y me llamo {nombre}")  # Otra forma de hacerlo (admite formteo de variables)

